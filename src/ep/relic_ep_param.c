static int ep_param_get_coeffs(fp_t *coeffs, const char *str) {
	if (str[0] == '\0') {
		/* need nonzero strlen */
		THROW(ERR_NO_VALID);
	}
	int degree = 0;
	unsigned offset = 0;
	for (; degree < RLC_EP_ISOMAP_MAX; ++degree) {
		const char *end = strchr(str + offset, ';');
		if (end == NULL) {
			/* last one */
			fp_read_str(coeffs[degree], str + offset, strlen(str + offset), 16);
			break;
		}
		unsigned len = end - str - offset;
		fp_read_str(coeffs[degree], str + offset, len, 16);
		offset += len + 1; /* move to after ';' */
	}
	if (degree == RLC_EP_ISOMAP_MAX) {
		/* ran out of space before converting all coeffs */
		THROW(ERR_NO_VALID);
	}
	return degree;
}

static void ep_param_set_isomap(const char *a_str, const char *b_str,
								const char *xn_str, const char *xd_str,
								const char *yn_str, const char *yd_str) {
	/* coefficients of isogenous curve */
	fp_read_str(ep_curve_get_iso_a(), a_str, strlen(a_str), 16);
	fp_read_str(ep_curve_get_iso_b(), b_str, strlen(b_str), 16);

	/* isogeny map coeffs */
	isomap_t iso_coeffs = ep_curve_get_iso_coeffs();
	iso_coeffs->deg_xn = ep_param_get_coeffs(iso_coeffs->xn, xn_str);
	iso_coeffs->deg_xd = ep_param_get_coeffs(iso_coeffs->xd, xd_str);
	iso_coeffs->deg_yn = ep_param_get_coeffs(iso_coeffs->yn, yn_str);
	iso_coeffs->deg_yd = ep_param_get_coeffs(iso_coeffs->yd, yd_str);
}
